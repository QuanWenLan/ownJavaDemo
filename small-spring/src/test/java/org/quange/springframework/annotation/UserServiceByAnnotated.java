package org.quange.springframework.annotation;

import org.quange.springframework.context.stereotype.Component;

import java.util.Random;

/**
 * @author Lan
 * @createTime 2023-08-29  11:49
 **/
@Component(value = "userService")
public class UserServiceByAnnotated implements IUserServiceAnnotated {
    private String token;

    public String queryUserInfo() {
        try {
            Thread.sleep(new Random(1).nextInt(100));
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        return "小傅哥，100001，深圳";
    }

    public String register(String userName) {
        try {
            Thread.sleep(new Random(1).nextInt(100));
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        return "注册用户：" + userName + " success！";
    }

    @Override
    public String toString() {
        return "UserService#token = { " + token + " }";
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
